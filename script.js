// Intersection Observer for animations
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
        }
    });
}, observerOptions);

// Add animation classes to elements
document.addEventListener('DOMContentLoaded', () => {
    // Add fade-in animation to sections
    const sections = document.querySelectorAll('section');
    sections.forEach(section => {
        section.classList.add('fade-in');
        observer.observe(section);
    });

    // Add slide-in animations to cards
    const cards = document.querySelectorAll('.service-card, .portfolio-card, .advantage-card');
    cards.forEach(card => {
        card.classList.add('scale-in');
        observer.observe(card);
    });

    // Add slide-in-left to about content
    const aboutText = document.querySelector('.about-text');
    if (aboutText) {
        aboutText.classList.add('slide-in-left');
        observer.observe(aboutText);
    }

    // Add slide-in-right to about image
    const aboutImage = document.querySelector('.about-image');
    if (aboutImage) {
        aboutImage.classList.add('slide-in-right');
        observer.observe(aboutImage);
    }

    // Add slide-in animations to contact items
    const contactItems = document.querySelectorAll('.contact-item');
    contactItems.forEach((item, index) => {
        item.classList.add('slide-in-left');
        item.style.animationDelay = `${index * 0.2}s`;
        observer.observe(item);
    });

    // Add slide-in-right to contact form
    const contactForm = document.querySelector('.contact-form');
    if (contactForm) {
        contactForm.classList.add('slide-in-right');
        observer.observe(contactForm);
    }
});

// Counter animation for stats
function animateCounters() {
    const counters = document.querySelectorAll('.stat h4');
    counters.forEach(counter => {
        const target = counter.textContent;
        if (target === '2021' || target === '100%') {
            let current = 0;
            const increment = target === '2021' ? 1 : 1;
            const timer = setInterval(() => {
                current += increment;
                if (target === '2021') {
                    counter.textContent = current;
                    if (current >= 2021) {
                        clearInterval(timer);
                    }
                } else {
                    counter.textContent = current + '%';
                    if (current >= 100) {
                        clearInterval(timer);
                    }
                }
            }, 20);
        }
    });
}

// Trigger counter animation when stats section is visible
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            animateCounters();
            statsObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

const statsSection = document.querySelector('.stats');
if (statsSection) {
    statsObserver.observe(statsSection);
}

